#include "libft.h"
#include <stdlib.h>

char	*ft_strrchr(char const *s, int c)
{
	size_t len;

	len = ft_strlen(s);
	if (c == '\0')
		return ((char *)&s[len]);
	while (len--)
	{
		if (s[len] == c)
			return ((char *)&s[len]);
	}
	return (NULL);
}
